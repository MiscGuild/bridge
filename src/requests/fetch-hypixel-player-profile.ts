export interface FetchError {
    status: number;
    statusText: string;
}

export default async function fetchHypixelPlayerProfile(
    usernameUUID: string
): Promise<HypixelPlayerResponse | FetchError> {
    const queryParam = usernameUUID.length === 32 ? `uuid=${usernameUUID}` : `name=${usernameUUID}`;

    const response = await fetch(
        `https://api.hypixel.net/player?key=${env.HYPIXEL_API_KEY}&${queryParam}`
    );

    if (response.status === 200) {
        const data = await response.json();
        if (data && data.player !== null) {
            return data.player as HypixelPlayerResponse;
        }
        return {
            status: 404,
            statusText: 'Player not found',
        };
    }

    return {
        status: response.status,
        statusText: response.statusText,
    };
}

